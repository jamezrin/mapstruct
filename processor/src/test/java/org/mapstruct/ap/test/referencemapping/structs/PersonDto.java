package org.mapstruct.ap.test.referencemapping.structs;

/**
 * Target class for testing ReferenceMapping functionality.
 *
 * @author MapStruct Authors
 */
public class PersonDto {

    private String name;
    private int age;
    private String email;

    public PersonDto() {
    }

    public PersonDto(String name, int age, String email) {
        this.name = name;
        this.age = age;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
